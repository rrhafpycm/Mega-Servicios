        private void Progreso(){
        
        final Thread t;
        
        t = new Thread(new Runnable() {
            @Override
            public void run() {
                
                jProgressBar1.setStringPainted(true);
                int x = 1;
                
                while(x <= 100){
                    jProgressBar1.setValue(x);      
                    
                    if (x == 10){
                         jLabel2.setText("a");
                    }
                    
                    if (x == 30){
                         jLabel2.setText("b");
                    }
                    
                    if (x == 70){
                         jLabel2.setText("c");
                    }
                    
                    if (x == 100){
                         jLabel2.setText("d");
                    }
                     
                    try {
                        Thread.sleep(500);
                    } catch (InterruptedException e) {
                        
                    }
                    x++;
                }
            }
        });
        t.start();
    }
    
